package ejemplos.bucles;

public class Contar1a10For {

	public static void main(String[] args) {
		// contamos del 1 al 10 con un bucle for
		System.out.println("conta,mos del 1 al 10 ");
		for(int conta=1; conta<=10; conta++)
			System.out.println(conta);
		System.out.println("\ncontamos del 1 al 10 ");
		for(int conta=10; conta>=1; conta--)
			System.out.println(conta);
	}

}
