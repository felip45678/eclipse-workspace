package ejemplos;

public class Longitudes {

	public static void main(String[] args) {
		// Definicion y declaracion de datos
		final double PI=3.1416;
		int radio;
		double longitud;
		//Calculamos la longitud de la circunferencia
		radio=2;
		longitud=2*PI*radio;
		System.out.println("Longitud de la circunferencia de radio "+radio+": "+longitud);

		radio=3;
		longitud=2*PI*radio;
		System.out.println("Longitud de la circunferencia de radio "+radio+": "+longitud);

		radio=4;
		longitud=2*PI*radio;
		System.out.println("Longitud de la circunferencia de radio "+radio+": "+longitud);

		radio=5;
		longitud=2*PI*radio;
		System.out.println("Longitud de la circunferencia de radio "+radio+": "+longitud);

	}

}
