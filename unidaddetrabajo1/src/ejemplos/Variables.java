package ejemplos;

public class Variables {

	public static void main(String[] args) {
        int edad=20;                        //variable de tipo entero(numero sin decimales)
        String nombre="Juan Ros 3";         //variable de tipo String(cadena de caracteres)
        double nota=8.5;                    //variable de tipo tipo real(numero con decimales)
       
        
        System.out.println("Nombre: "+nombre);
        System.out.println("nota: "+nota);
        System.out.println("Edad: "+edad);
        nota=nota -1;
        System.out.println("nueva Nota: "+nota);
	}

}
