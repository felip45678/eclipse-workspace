package ejemplos;

public class Operadoresasignacion {

	public static void main(String[] args) {
		// declaracion e inicializacion de variavbles 
		int a=8, b=5;
		
		a+=2;						//a=a+2
		System.out.println(a);		//10
		
		a-=b;						//a=a-b
		System.out.println(a);		//5

		--b;						//b=b-1
		System.out.println(b);		//4

		b--;
		System.out.println(b);		//3

	}

}
