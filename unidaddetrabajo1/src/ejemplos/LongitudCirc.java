package ejemplos;

public class LongitudCirc {

	public static void main(String[] args) {
		// Declaracion e inicializacion de datos
		final double PI=3.1416;
				int radio=3;

				
		//Hacemos los calculos y los mostramos por pantalla 
			double longitud=2*PI*radio;
			System.out.println("Longitud de una circunferencia de radio "+radio+" :"+longitud);
	}

}
