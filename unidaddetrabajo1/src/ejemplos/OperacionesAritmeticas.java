package ejemplos;

public class OperacionesAritmeticas {

	public static void main(String[] args) {
		//declaracion e inicializacion de variables
		int a=5;
		int b=3;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
		double c=5;
		int d=2;
		System.out.println(c+d);
		System.out.println(c-d);
		System.out.println(c*d);
		System.out.println(c/d);
	}

}
